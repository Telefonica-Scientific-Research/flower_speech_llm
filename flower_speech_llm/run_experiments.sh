#!/bin/bash
# =============================================================================
# Run FL experiments A1/B1/B2 on MLS dataset
#
# Prerequisites:
#   1. Run create_experiment_partitions.py first to generate data dirs
#   2. Ensure pyproject.toml is in the current directory
#
# Usage:
#   ./run_experiments.sh          # Run all three experiments sequentially
#   ./run_experiments.sh A1       # Run only Setting A1
#   ./run_experiments.sh B1       # Run only Setting B1
#   ./run_experiments.sh B2       # Run only Setting B2
#   ./run_experiments.sh eval A1  # Evaluate A1 checkpoint on test set
# =============================================================================

set -e

# ---- Configuration ----
NUM_ROUNDS=200
LOCAL_EPOCHS=10
MAX_LR=0.0001
FRACTION_FIT=0.3
FEDPROX_MU=0.01

# Data directories (generated by create_experiment_partitions.py)
A1_TRAIN_DIR="./fl_A1_mixed_316"
B1_TRAIN_DIR="./fl_B1_speaker_316"
DEV_DIR="./fl_dev_316"
TEST_DIR="./fl_MLS_test"

# Checkpoint directories (one per experiment)
A1_CKPT_DIR="FL_checkpoints_A1_mixed"
B1_CKPT_DIR="FL_checkpoints_B1_speaker_fedavg"
B2_CKPT_DIR="FL_checkpoints_B2_speaker_fedprox"

# ---- Functions ----
run_a1() {
    echo "============================================================"
    echo "  EXPERIMENT A1: Multilingual-mixed IID + FedAvg"
    echo "============================================================"
    flwr run . --run-config \
        "csv-train-dir=${A1_TRAIN_DIR} csv-dev-dir=${DEV_DIR} \
         checkpoint-dir=${A1_CKPT_DIR} \
         num-server-rounds=${NUM_ROUNDS} local-epochs=${LOCAL_EPOCHS} \
         max-lr=${MAX_LR} fraction-fit=${FRACTION_FIT} \
         fedprox-mu=0.0"
}

run_b1() {
    echo "============================================================"
    echo "  EXPERIMENT B1: One-speaker + FedAvg"
    echo "============================================================"
    flwr run . --run-config \
        "csv-train-dir=${B1_TRAIN_DIR} csv-dev-dir=${DEV_DIR} \
         checkpoint-dir=${B1_CKPT_DIR} \
         num-server-rounds=${NUM_ROUNDS} local-epochs=${LOCAL_EPOCHS} \
         max-lr=${MAX_LR} fraction-fit=${FRACTION_FIT} \
         fedprox-mu=0.0"
}

run_b2() {
    echo "============================================================"
    echo "  EXPERIMENT B2: One-speaker + FedProx (mu=${FEDPROX_MU})"
    echo "============================================================"
    flwr run . --run-config \
        "csv-train-dir=${B1_TRAIN_DIR} csv-dev-dir=${DEV_DIR} \
         checkpoint-dir=${B2_CKPT_DIR} \
         num-server-rounds=${NUM_ROUNDS} local-epochs=${LOCAL_EPOCHS} \
         max-lr=${MAX_LR} fraction-fit=${FRACTION_FIT} \
         fedprox-mu=${FEDPROX_MU}"
}

run_eval() {
    local setting=$1
    local ckpt_dir

    case $setting in
        A1) ckpt_dir=${A1_CKPT_DIR} ;;
        B1) ckpt_dir=${B1_CKPT_DIR} ;;
        B2) ckpt_dir=${B2_CKPT_DIR} ;;
        *)  echo "Unknown setting: $setting"; exit 1 ;;
    esac

    echo "============================================================"
    echo "  EVALUATE: Setting ${setting}"
    echo "============================================================"
    python evaluate_fl_model.py \
        --checkpoint "${ckpt_dir}/final_model.pt" \
        --test-dir "${TEST_DIR}" \
        --output-json "eval_results_${setting}.json"
}

# ---- Main ----
if [ $# -eq 0 ]; then
    echo "Running all experiments sequentially: A1 → B1 → B2"
    echo ""
    run_a1
    run_eval A1
    run_b1
    run_eval B1
    run_b2
    run_eval B2
    echo ""
    echo "All experiments complete. Results:"
    echo "  eval_results_A1.json"
    echo "  eval_results_B1.json"
    echo "  eval_results_B2.json"
elif [ "$1" = "eval" ]; then
    run_eval "$2"
elif [ "$1" = "A1" ]; then
    run_a1
elif [ "$1" = "B1" ]; then
    run_b1
elif [ "$1" = "B2" ]; then
    run_b2
else
    echo "Usage: $0 [A1|B1|B2|eval <A1|B1|B2>]"
    exit 1
fi
